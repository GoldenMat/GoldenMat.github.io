		<h4>Accesso</h4>
		
		<p>Da Lecco percorrere la SS36 fino a Colico, per poi imboccare la val Chiavenna fino all'omonima cittadina. In centro a Chiavenna dirigersi a destra (direzione dogana Svizzera), superando i vari nuclei del paese di Piuro, fino a giungere a Villa di Chiavenna. Appena superato il laghetto artificiale sul fiume Mera, prima della dogana, si imbocca il ponte sulla destra che consente di arrivare alla frazione San Barnaba, dove parte la strada agro silvio pastorale (ma ben asfaltata) che sale agli alpeggi del comune. La strada arriva prima alla frazione Canete, poi sale con diversi tornanti, e bisogna prendere la seconda deviazione a destra (guardare le indicazioni) fino alla località Cantone, dove si trova un ampio parcheggio per lasciare la macchina. Quest'ultimo tratto è transitabile solo pagando un permesso online, 5€ nel 2023, informarsi preventivamente sul sito del comune.</p>
		
