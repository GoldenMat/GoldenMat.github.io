		<h4>Accesso</h4>
		
		<p>Da Lecco salire in Valsassina. A Balisio seguire indifferentemente dritto o a destra, per poi portarsi rispettivamente salendo a destra o scendendo a sinistra fino a Barzio. Seguire le indicazioni per la funivia dei Piani di Bobbio e parcheggiare (a pagamento) nel grande piazzale. Se non ci fosse posto o non si volesse pagare è possibile parcheggiare presso il cimitero di Barzio (pochi posti) oppure nei parcheggi in centro paese e sfruttare la navetta (gratuita) che porta fino al piazzale.</p>
		
