        <h4>Accesso</h4>
        
        <p>Da Lecco salire in Valsassina. All'altezza di Balisio seguire verso Barzio (salendo o scendendo è uguale, si ricongiungono). Parcheggiare idealmente al parcheggio della funivia (a pagamento) o al cimitero di Barzio - attenzione nei periodi di apertura delle piste da sci in quanto a un certo punto le macchine vengono dirottate verso i parcheggi in centro a Barzio (disponibili comunque navette gratuite). Salire ai Piani di Bobbio in funivia (o seguendo la strada che parte a destra dell'ingresso del parcheggio, calcolare circa 1h30/2h aggiuntive). Possibile anche raggiungere i Piani di Bobbio da Valtorta, sicuramente soluzione scomoda per chi come noi arriva da Lecco ma permette di fare meno dislivello senza funivia.</p>
        
        <h4>Avvicinamento</h4>
        
        Dall'arrivo della funivia portarsi in salita sulla pista per poi prendere verso destra una stradina nel bosco. Superare il Rif. Sora-Casari e attraversare le piste nei pressi del rifugio Stella, facendo attenzione ad eventuali sciatori, poi costeggiare la pista Camosci fino al rif. Lecco. Inoltrarsi nell'evidente vallone dei Camosci, spostandosi presto sull'altro versante per ripidi pendii puntando ad un evidente largo canaletto che porta senza grosse difficoltà all'attacco della cresta, presso una cengia con un grosso sasso triangolare (resinati visibili). Possibile anche raggiungere l'attacco da sinistra, portandosi alla base delle varie torri del Pesciola per poi costeggiare la parete verso destra (soluzione estiva).
        
