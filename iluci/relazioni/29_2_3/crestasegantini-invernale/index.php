        <h4>Accesso</h4>
        
        <p>Da Lecco salire in Valsassina fino a Ballabio. Alla rotonda all'ingresso del paese prendere verso sinistra e subito dopo a destra salendo poi per i tornanti fino ai Piani Resinelli. Superato il piazzale (grande parcheggio) all'altezza della chiesa prendere una stretta curva verso destra e seguire la ripida strada fino al suo termine, dove si parcheggia (pochi posti). Nel caso non ci fosse posto parcheggiare sugli altri spiazzi salendo oppure tornare al grande piazzale e partire da lì.</p>
        
