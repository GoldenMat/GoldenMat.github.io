        <h4>Via</h4>

        <p>La seguente relazione è solo uno dei modi possibili di percorrere la Cresta OSA, personalmente (Oracolo) essendo la via che ho ripetuto di più, avendola sopra casa, questo è il modo in cui negli anni ho avuto modo di verificare è il miglior mix tra velocità e sicurezza, ottimo soprattutto per portare delle persone alle prime armi. Ovvio che l'affollamento sulla via o le preferenze personali possono far cambiare il piano, ma se non si conoscesse proprio la via consiglio di fare così, evitando punti di sosta scomodi o di far conserva dove magari qualcuno alle prime armi potrebbe subire l'esposizione.</p>

