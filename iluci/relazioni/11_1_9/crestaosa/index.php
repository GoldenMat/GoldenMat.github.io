        <h4>Accesso</h4>
                            
        <p>Da Lecco attraversare il ponte Kennedy portandosi a Malgrate; salire per la superstrada fino a quando, superato l'Obi, si entra a Valmadrera. Percorrere tutta la strada curvando a sinistra nei pressi della chiesa, per poi prendere a destra via Leopardi (indicazioni San Tomaso) che va seguita fino a un bivio dal quale si prende a destra via san Carlo Borromeo. Seguirla fino a quando la strada diventa solo per autorizzati, dove si può parcheggiare la macchina (pochi posti). Nel caso non ci fosse posto è possibile lasciarla più sotto (via Caduti per la Libertà) o addirittura anche in centro a Valmadrera.</p>

        <h4>Avvicinamento</h4>

        <div class="halved-grid">
            <div class="halved-block-text">Dal primo parcheggio citato salire la carrareccia che ripida ma senza difficoltà porta fino alla Cappelletta VARS, crocevia dei sentieri sopra Valmadrera. Si prende il sentiero che passa sul fianco sinistro della cappelletta (indicazioni) che attraverso un lungo bosco, a tratti scivoloso a causa dei numerosi passaggi, restando sempre sul sentiero principale, porta comodamente fino alla fresca fonte di Sambrosera dove è possibile fare una pausa ristoratrice. Tornare brevemente sul sentiero e prendere a sinistra (indicazioni), inerpicandosi su sentiero ripido ma non difficile che attraversa il versante Sud del Moregallo. Giunti a una forcella (altre indicazioni), prendere a sinistra per traccia come da cartello aggirando un iniziale sperone sulla sinistra, e risalendo un'ultima zona franata (residui della frana del 2020) si arriva all'attacco della via.</div>
            
