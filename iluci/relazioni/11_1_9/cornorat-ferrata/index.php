		<h4>Accesso</h4>
		
		<p>Da Lecco attraversare il ponte Kennedy portandosi a Malgrate; salire per la superstrada fino a quando, superato l'Obi, si entra a Valmadrera. Percorrere tutta la strada curvando a sinistra nei pressi della chiesa, per poi prendere a destra via Leopardi (indicazioni San Tomaso) che va seguita fino a un bivio dal quale si prende a destra via san Carlo Borromeo. Seguirla fino a quando la strada diventa solo per autorizzati, dove si può parcheggiare la macchina (pochi posti). Nel caso non ci fosse posto è possibile lasciarla più sotto (via Caduti per la Libertà) o addirittura anche in centro a Valmadrera.</p>
		
